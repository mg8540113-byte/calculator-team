

def subtraction(a,b):
    return a - b


def add(a,b):
    return a + b


def multiplication(a,b):
    return a * b

def power(a,b):
    return a ** b

def division(a,b):
    return a / b


def root(a):
    return a ** 0.5

def Absolute_number(a):
    if a < 0:
        return a * -1
    else:
        return a



